# ── Helpers ───────────────────────────────────────────────────────────────── #

def _get_version(cmd):
    try:
        out = subprocess.check_output(cmd, stderr=subprocess.STDOUT, text=True)
        return out.splitlines()[0].strip()
    except Exception:
        return "unknown"


def _is_installed(binary_path):
    return os.path.isfile(binary_path) and os.access(binary_path, os.X_OK)


def _write_modulefile(path, content):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as f:
        f.write(content)


# ── Check all software ────────────────────────────────────────────────────── #

def check_all():
    """
    Check which software is installed.
    Returns dict: { name: { installed, version, path } }
    """
    status = {}
    for name, cfg in SOFTWARE.items():
        installed = _is_installed(cfg["binary_path"])
        version   = _get_version(cfg["version_cmd"]) if installed else "not found"
        status[name] = {
            "installed": installed,
            "version":   version,
            "path":      cfg["binary_path"],
        }
    return status


# ── Create modulefiles ────────────────────────────────────────────────────── #

def create_modulefile(name):
    """Create a modulefile for a given software name."""
    cfg = SOFTWARE.get(name)
    if not cfg:
        print(f"[module] unknown software: {name}")
        return None

    if not _is_installed(cfg["binary_path"]):
        print(f"[module] ✘ {name} not installed — skipping modulefile")
        return None

    binary_dir = os.path.dirname(cfg["binary_path"])
    lib_path   = cfg["lib_path"]
    mod_path   = os.path.join(MODULES_BASE, cfg["module_dir"], cfg["module_name"])

    # skip if modulefile already exists
    if os.path.isfile(mod_path):
        print(f"[module] ✔ modulefile already exists : {mod_path}")
        return mod_path

    content = f"""#%Module1.0
# Auto-generated by Caribou
# Software : {name}
# Binary   : {cfg['binary_path']}

proc ModulesHelp {{}} {{
    puts stderr "{name} - auto-generated by Caribou"
    puts stderr "Binary: {cfg['binary_path']}"
}}

module-whatis "{name}"

prepend-path PATH            {binary_dir}
prepend-path LD_LIBRARY_PATH {lib_path}
"""
    _write_modulefile(mod_path, content)
    print(f"[module] ✔ modulefile created : {mod_path}")
    return mod_path


def create_all_modulefiles():
    """Create modulefiles for all installed software."""
    print("\n[module] Creating modulefiles for all installed software ...")
    for name in SOFTWARE:
        create_modulefile(name)


# ── Create caribou benchmark modulefiles ─────────────────────────────────── #

def create_stream_module(binary_path):
    """Create modulefile for STREAM benchmark."""
    binary_dir  = os.path.dirname(os.path.abspath(binary_path))
    module_path = os.path.join(CARIBOU_DIR, "stream")
    os.makedirs(CARIBOU_DIR, exist_ok=True)

    content = f"""#%Module1.0
# Auto-generated by Caribou
# Benchmark : STREAM (memory bandwidth)
# Binary    : {binary_path}

proc ModulesHelp {{}} {{
    puts stderr "STREAM memory bandwidth benchmark"
    puts stderr "Binary: {binary_path}"
}}

module-whatis "STREAM memory bandwidth benchmark"

prepend-path PATH            {binary_dir}
prepend-path LD_LIBRARY_PATH {binary_dir}

setenv STREAM_BINARY {binary_path}
"""
    _write_modulefile(module_path, content)
    print(f"[module] ✔ modulefile created : {module_path}")
    print(f"[module]   usage              : module load caribou/stream")
    return module_path


