# ── Show module avail table ───────────────────────────────────────────────── #

def show_module_avail():
    """
    Show a table like `module avail` for all software
    including caribou benchmarks.
    """
    status = check_all()

    print(f"\n{'='*60}")
    print(f"  Module Availability (Caribou)")
    print(f"{'='*60}")
    print(f"  {'Module':<25} {'Status':<15} {'Version'}")
    print(f"  {'─'*55}")

    # system software
    for name, cfg in SOFTWARE.items():
        s        = status[name]
        mod_dir  = SOFTWARE[name]["module_dir"]
        mod_name = SOFTWARE[name]["module_name"]
        full_mod = f"{mod_dir}/{mod_name}"
        marker   = "✔" if s["installed"] else "✘"
        ver      = s["version"][:35] if s["installed"] else "not installed"
        print(f"  {marker} {full_mod:<23} {'installed' if s['installed'] else 'missing':<15} {ver}")

    # caribou benchmarks
    print(f"  {'─'*55}")
    for bname in ["stream", "hpl-intel", "hpl-aocc"]:
        mod_path = os.path.join(CARIBOU_DIR, bname)
        exists   = os.path.isfile(mod_path)
        marker   = "✔" if exists else "✘"
        status_s = "available" if exists else "not built"
        print(f"  {marker} {'caribou/' + bname:<23} {status_s:<15}")

    print(f"{'='*60}\n")


# ── Main (run standalone) ─────────────────────────────────────────────────── #

if __name__ == "__main__":
    import argparse
    parser = argparse.ArgumentParser(
        description="Caribou Module Creator"
    )
    parser.add_argument("--check",       action="store_true", help="check all software")
    parser.add_argument("--create-all",  action="store_true", help="create all modulefiles")
    parser.add_argument("--avail",       action="store_true", help="show module avail table")
    parser.add_argument("--create",      type=str,            help="create modulefile for specific software")
    args = parser.parse_args()

    if args.check:
        status = check_all()
        print(f"\n  {'Software':<15} {'Installed':<12} {'Path'}")
        print(f"  {'─'*55}")
        for name, s in status.items():
            mark = "✔" if s["installed"] else "✘"
            print(f"  {mark} {name:<13} {str(s['installed']):<12} {s['path']}")
        print()

    elif args.create_all:
        create_all_modulefiles()
        show_module_avail()

    elif args.avail:
        show_module_avail()

    elif args.create:
        create_modulefile(args.create)

    else:
        parser.print_help()
